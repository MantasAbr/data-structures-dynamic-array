    /**
     * Spausdinimo metodas
     * @param autos Sarasas
     * @param komentaras Komentaras virs lenteles
     */
    public void Spausdinti(DinaminisSarasas<Automobilis> autos, String komentaras){
        
        if(autos.count() > 0){                    
            String linija = "---------------------------------------------------------------------------------------\n";
            String linija1 = "Markė       Modelis    Kuro       Likęs     Kaina         Ar      Stovėjimo\n";
            String linija2 = "                       tipas      kuras     Eur/min     užimta?   adresas\n";
        
            textArea.append(komentaras + "\n");
            textArea.append(linija);
            textArea.append(linija1);
            textArea.append(linija2);
            textArea.append(linija);

            for(int i = 0; i < autos.count(); i++){
                textArea.append(autos.get(i).toString() + "\n");
            }
            textArea.append(linija);
            textArea.append("\n");
        }
        else
            textArea.append("\nTusčias sąrašas");
    }
    
    /**
     * Duomenu failo nuskaitymas
     * @param auto Objektu sarasas
     */
    public void Nuskaityti(DinaminisSarasas<Automobilis> auto){
        try{
            FileReader reader = new FileReader("Duomenys.txt");
            BufferedReader bufferedReader = new BufferedReader(reader);
 
            String line;
 
            while ((line = bufferedReader.readLine()) != null){
                String[] dalys = line.split(";");
                
                String marke = dalys[0];
                String modelis = dalys[1];
                String kuroTipas = dalys[2];
                double likesKuras = Double.parseDouble(dalys[3]);
                double kaina = Double.parseDouble(dalys[4]);
                String arUzimta = dalys[5];
                String adresas;
                if(arUzimta.equals("Ne"))
                    adresas = dalys[6];
                else
                    adresas = "-";
                
                Automobilis automobilis = new Automobilis(marke, modelis, 
                        kuroTipas, likesKuras, kaina, arUzimta, adresas);
                
                auto.add(automobilis);
                }
        }
        catch(IOException e){
            e.printStackTrace();
        }                  
    }
    
    /**
     * ComboBox'u atnaujinimo metodas po ivairiu objektu pasalinimu/pridejimu
     * @param auto 
     */
    public void ComboBoxRefresh(DinaminisSarasas<Automobilis> auto){
        comboBox.removeAllItems();
        comboBox2.removeAllItems();
        for(int i = 0; i < auto.count(); i++){
            comboBox.addItem(Integer.toString(i + 1));
            comboBox2.addItem(Integer.toString(i + 1));
        }
    }
    
    /**
     * Suformuojamas naujas sarasas pagal user'io inputus
     * @param auto Pradinis sarasas
     * @param naujas Naujas sarasas
     * @param pinigai User'io parinkta pinigu suma
     * @param intervalas User'io parinktas laiko intervalas
     */
    public void Suformuoti(DinaminisSarasas<Automobilis> auto, 
                           DinaminisSarasas<Automobilis> naujas, 
                           double pinigai, String intervalas){
        
        double kainaUzMinute;
        int minutes;
        
        //Paverciame norima nuomos intervala i skaitine verte
        String intervaloEilute[] = intervalas.split(" ");
        
        //Pasirinktos minutes
        if(intervaloEilute[1].equals("min.")){
            minutes = Integer.parseInt(intervaloEilute[0]);
            kainaUzMinute = pinigai / minutes;
        }
        //Pasirinktos valandos
        else{
            minutes = Integer.parseInt(intervaloEilute[0]) * 60;
            kainaUzMinute = pinigai / minutes;
        }
        
        for(int i = 0; i < auto.count(); i++){
            if(auto.get(i).getArUzimta().equals("Ne") && auto.get(i).getKaina() <= kainaUzMinute){
                naujas.add(auto.get(i));
            }
        }
    }
    

