    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        //Sukuriamas naujas user'io Auto objektas ir ikeliamas i sarasa
        //pasirinktame elemente
        
        //Susirenkami user'io inputai
        Automobilis naujas;
        String userInput = textField.getText();
        String[] parts = userInput.split(" ");
        int index = comboBox2.getSelectedIndex();
        
        String modelis = parts[0];
        String marke = parts[1];
        String kuroTipas = parts[2];
        double likesKuras = Double.parseDouble(parts[3]);
        double kaina = Double.parseDouble(parts[4]);
        String arUzimta = parts[5];
        String adresas;
        if(arUzimta.equals("Taip"))
            adresas = "-";
        else
            adresas = parts[6];
        
        naujas = new Automobilis(modelis, marke, kuroTipas, likesKuras, kaina, arUzimta, adresas);
        
        autos.addAt(index, naujas);
        Spausdinti(autos, "Sąrašas su nauju objektu");
        ComboBoxRefresh(autos);
        
    }//GEN-LAST:event_jButton3ActionPerformed

